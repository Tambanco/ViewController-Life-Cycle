import UIKit

public func buttonConfigurator(button: UIButton) {
    button.layer.cornerRadius = 10
    button.layer.borderWidth = 1
    button.layer.borderColor = #colorLiteral(red: 0.05882352963, green: 0.180392161, blue: 0.2470588237, alpha: 1)
}
